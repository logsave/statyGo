package main

import "fmt"

func main() {
	a := 9
	b := 3

	fmt.Println(a + b)
	fmt.Println(a - b)
	fmt.Println(a * b)
	fmt.Println(a / b)
	fmt.Println(a % b)

	fmt.Println(a > b)
	fmt.Println(a >= b)
	fmt.Println(a < b)
	fmt.Println(a <= b)
	fmt.Println(a == b)
	fmt.Println(a != b)

	fmt.Println(!true)
	fmt.Println(true && false)
	fmt.Println(false || true)
// ++ --

// & | ^ << >>
	fmt.Println(5 & 2)
	fmt.Println(5 | 1)
	fmt.Println(8 | 5)
// <<= >>= &= ^= |=





}
